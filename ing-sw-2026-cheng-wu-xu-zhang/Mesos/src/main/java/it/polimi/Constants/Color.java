package it.polimi.Constants;

/**
 * Represents the distinct colors that can be assigned to players.
 * Used for identifying player pieces, totems, and board positions.
 */
public enum Color {
    /** The blue player color. */
    BLUE,
    /** The orange player color. */
    ORANGE,
    /** The purple player color. */
    PURPLE,
    /** The white player color. */
    WHITE,
    /** The yellow player color. */
    YELLOW
}
