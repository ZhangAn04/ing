package it.polimi.Constants;

/**
 * Defines the possible positions of a card or a game element.
 * Primarily used to identify the location within the offer or a deck.
 */
public enum Position {
    /** Top position in the offer or display. */
    TOP,
    /** Bottom position in the offer or display. */
    BOTTOM,
    /** Position within the deck. */
    DECK;
}
