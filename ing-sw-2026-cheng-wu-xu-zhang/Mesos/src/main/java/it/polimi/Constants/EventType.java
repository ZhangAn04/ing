package it.polimi.Constants;

/**
 * Enumerates the types of events that can occur during the game.
 * Events trigger specific game mechanics like sustenance or hunting.
 */
public enum EventType {
    /** Event related to food consumption and tribe survival. */
    SUSTENANCE,
    /** Event related to food production and resource gathering. */
    HUNTING,
    /** Event related to spiritual or cultural ceremonies. */
    RITUAL,
    /** Event related to artistic expression and long-term achievements. */
    ROCK_PAINTINGS;
}
