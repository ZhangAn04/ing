package it.polimi.Constants;

/**
 * Defines the various types of characters available in the game.
 * Each type has unique properties or triggers different building and event effects.
 */
public enum CharacterType {
    /** Specialized in technological icons. */
    INVENTOR,
    /** Specialized in resource discounts. */
    GATHERER,
    /** Specialized in spiritual stars. */
    SHAMAN,
    /** Specialized in building construction bonuses. */
    BUILDER,
    /** Represents artistic diversity in the tribe. */
    ARTIST,
    /** Specialized in hunting icons and food supply. */
    HUNTER,
    /** Represents the absence of a character type. */
    NONE;
}
